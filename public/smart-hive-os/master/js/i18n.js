  // ==================== دالة الترجمة العامة ====================
  function osT(key) {
    const lang = localStorage.getItem('os-language') || 'en';
    const keys = key.split('.');
    let result = OS_I18N[lang];
    
    for (const k of keys) {
      if (result && result[k] !== undefined) {
        result = result[k];
      } else {
        return key;
      }
    }
    
    return result;
  }
  
  // ==================== تغيير اللغة ====================
  function changeOSLanguage(lang) {
    localStorage.setItem('os-language', lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    applyOSTranslations();
  }
  
  // ==================== تطبيق الترجمة عند التحميل ====================
  function applyOSTranslations() {
    const lang = localStorage.getItem('os-language') || 'en';
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    
    document.querySelectorAll('[data-os-i18n]').forEach(el => {
      const key = el.getAttribute('data-os-i18n');
      const text = osT(key);
      if (text) el.textContent = text;
    });
    
    document.querySelectorAll('[data-os-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-os-i18n-placeholder');
      const text = osT(key);
      if (text) el.placeholder = text;
    });
  }
  
  // تطبيق الترجمة عند تحميل الصفحة
  document.addEventListener('DOMContentLoaded', applyOSTranslations);